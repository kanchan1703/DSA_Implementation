# Hash Tables Implementation
# Hash tables are data structures with o(1) insertion adn deletion time
# In python, hash tables are implemented as dictionaries.

# In hash tables (dictionaries), data is stored in key-value pairs.

dictionary = dict()
dictionary = {'one':1, 'two':2, 'three':3, 'four':4, 'five':5}

print(dictionary)
print(dictionary.keys())
print(dictionary.values())

print(dictionary['one'])  # Accessing value using key